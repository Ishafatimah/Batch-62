# Define two numbers
a = 10
b = 5

# Perform addition
addition = a + b
print("Addition:", addition)

# Perform subtraction
subtraction = a - b
print("Subtraction:", subtraction)

# Perform multiplication
multiplication = a * b
print("Multiplication:", multiplication)

# Perform division
if b != 0:
    division = a / b
    print("Division:", division)
else:
    print("Error: Division by zero is not allowed")
